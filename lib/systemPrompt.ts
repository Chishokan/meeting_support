export function buildSystemPrompt(dept: string, name: string): string {
  const supplement = DEPT_SUPPLEMENTS[dept] ? `${DEPT_SUPPLEMENTS[dept]}\n` : '';
  return ASSISTANT_INSTRUCTIONS
    .replace('{{事業部別追記}}', supplement)
    .replace(/\{\{事業部\}\}/g, dept || '（事業部）')
    .replace(/\{\{担当\}\}/g, name || '（担当）');
}

